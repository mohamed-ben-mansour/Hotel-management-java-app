private void displayroom() throws ClassNotFoundException, SQLException{
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel", "root", "123456789");
    String query = "SELECT roomNo AS most_frequent_customer \n" +
"FROM ( \n" +
    "SELECT roomNo, COUNT(roomNo) AS appearance_count \n" +
    "FROM customer\n" +
    "GROUP BY roomNo\n " +
") AS subquery \n" +
"ORDER BY appearance_count DESC \n" +
"LIMIT 3";
    PreparedStatement pst = con.prepareStatement(query);
    ResultSet rs = pst.executeQuery();
    DefaultTableModel model = (DefaultTableModel)roomtab.getModel();
    model.setRowCount(0);
    while (rs.next()) {
       
     model.addRow(new String[]{rs.getString(1)});
    }
    pst.close();
    con.close();
    
}
